package com.LearnJavaPrograms.day1_Basics;

public class Calculator {

	// Declare the instance variable
	int result;

	public void addTowNumber(int number1, int number2) {

		System.out.println(result = number1 + number2);

	}

	public void subtractTowNumber(int number1, int number2) {

		System.out.println(result = number1 - number2);

	}

	public void mulTowNumber(int number1, int number2) {

		System.out.println(result = number1 * number2);

	}

	public void divTowNumber(int number1, int number2) {

		System.out.println(result = number1 / number2);

	}

	public void modTowNumber(int number1, int number2) {

		System.out.println(result = number1 % number2);

	}

}
